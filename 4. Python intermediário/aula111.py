'''
    'pip': instalador de pacotes do Python
    Comandos úteis:

        Instalar última versão: pip + install + 'nome do pacote'
        Desinstalar pacote: pip + uninstall + 'nome do pacote'

        Instalar versão específica: pip + install + 'nome do pacote==versão'
        Congelar (ver pacotes): pip + freeze

    requirements.txt: arquivo que serve para listar dependências necessárias p/ executar um projeto
    Comandos úteis:

        Gerar um requirements.txt: pip freeze > requirements.txt
        Instalar dependências do requirements.txt: pip + install -r + requirements.txt
'''