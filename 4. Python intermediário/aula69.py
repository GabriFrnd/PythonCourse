# Introdução ao tipo 'set' (conjuntos em Python)

'''
Anotações da aula:

    - Sets são mutáveis, mas aceitam apenas tipos imutáveis.
    - São eficientes para remover valores duplicados de iteráveis.

    - Os sets não têm índices, não garantem ordem e são iteráveis.
    - Métodos úteis: add, update, clear e discard.
'''

