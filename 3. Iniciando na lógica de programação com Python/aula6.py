# Introdução aos operadores aritméticos

adicao = 10 + 10
print('Resultado da adição:', adicao)

subtracao = 10 - 5
print('Resultado da subtração:', subtracao)

multiplicacao = 10 * 10
print('Resultado da multiplicação:', multiplicacao)

divisao = 10 / 2 # Retorna um float
print('Resultado da divisão:', divisao)

divisao_inteira = 10 // 2
print('Resultado da divisão inteira:', divisao_inteira)

potenciacao = 2 ** 3
print('Resultado da potenciação:', potenciacao)

resto = 10 % 3
print('Resultado do resto da divisão:', resto)